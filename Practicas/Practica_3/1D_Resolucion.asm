PIC EQU 20H
TIMER EQU 10H
PIO EQU 30H
N_CLK EQU 10

ORG 40
 IP_CLK DW RUT_CLK

ORG 1000H
 PATRON DB 0
 FINAL DB 0

ORG 3000H
 RUT_CLK: INC PATRON
 CMP PATRON, 0FFH
 JNZ LUCES
 MOV FINAL, 1
 MOV AL, 0FFH
 OUT PIC+1, AL
 JMP FIN
 LUCES: MOV AL, PATRON
 OUT PIO+1, AL
 MOV AL, 0
 OUT TIMER, AL
 FIN: MOV AL, 20H
 OUT PIC, AL
 
 IRET

ORG 2000H
CLI
MOV AL, 0FDH
OUT PIC+1, AL
MOV AL, N_CLK
MOV AL, 1
OUT TIMER+1, AL
MOV AL, 0
OUT PIO+3, AL
OUT PIO+1, AL
OUT TIMER, AL
STI
LAZO: CMP FINAL, 1
JNZ LAZO
INT 0

END